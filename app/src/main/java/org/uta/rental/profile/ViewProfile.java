package org.uta.rental.profile;

public class ViewProfile {
    private String userName;
    private String password;
    private String utaID;
    private String lastName;
    private String firstName;
    private String phone;
    private String role;
    private String email;
    private String streetAddress;
    private String city;
    private String state;
    private String zipCode;
    private String rentalprivilegeStatus;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getUtaID() {
        return utaID;
    }

    public void setUtaID(String utaID) {
        this.utaID = utaID;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getStreetAddress() {
        return streetAddress;
    }

    public void setStreetAddress(String streetAddress) {
        this.streetAddress = streetAddress;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getZipCode() {
        return zipCode;
    }

    public void setZipCode(String zipCode) {
        this.zipCode = zipCode;
    }

    public String getRentalprivilegeStatus() {
        return rentalprivilegeStatus;
    }

    public void setRentalprivilegeStatus(String rentalprivilegeStatus) {
        this.rentalprivilegeStatus = rentalprivilegeStatus;
    }
}
